def main():
    """
    This program demonstrates basic operations with matrices.
    """
    # Declaration of matrices
    matrix1 = [[1, 2, 3], [4, 5, 6]]

    # Printing the matrix
    print("Result Matrix:")
    for i in range(2):
        for j in range(3):
            print(matrix1[i][j], end=" ")
        print()


main()
